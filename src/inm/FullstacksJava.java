package org.inm;

public class FullstacksJava {

	private void coreJava() {
		System.out.println("coreJava");
	}

	private void advanceJava() {
		System.out.println("advanceJava");
	}

	private void javaScript() {
		System.out.println("javaScript");
	}

	private void springFramework() {
		System.out.println("springFramework");
	}

	public static void main(String[] args) {

		FullstacksJava obj = new FullstacksJava();
		obj.coreJava();
		obj.advanceJava();
		obj.javaScript();
		obj.springFramework();
	}

}
